def complex_function():
    """
    This function performs a complex operation using multiple loops.

    Parameters
    ----------
    None

    Returns
    -------
    result : int
        The result of the complex operation.

    """
    result = 0

    # Loop 1
    for i in range(10):
        result += i

    # Loop 2
    for j in range(10, 20):
        result += j

    # Loop 3
    for k in range(20, 30):
        result += k

    # Loop 4
    for l in range(30, 40):
        result += l

    # Loop 5
    for m in range(40, 50):
        result += m

    return result